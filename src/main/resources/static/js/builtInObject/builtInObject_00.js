/** 자바스크립트 객체의 분류 **/

/**
 자바스크립트 객체는 크게 3가지로 나뉜다고 합니다.
 1. 표준 빌트인 객체
    표준 빙트인 객체는 ECMAScript 사양에 정의된 객체를 말하며, 애플리케이션 전역의 공통 기능을 제공한다.
    이는 자바스크립트 실행 환경*과 관계없이 언제나 사용할 수 있다. 또 전역 객체의 프로퍼티로서 제공된다.
    따라서 별도의 선언 없이 전역 변수처럼 언제나 참조할 수 있다.

 2. 호스트 객체
    호스트 객체는 ECMAScript 사양에 정의되어 있지는 않지만 자바스크립트 실행환경*에서 추가로 제공하는 객체를 말한다.

 3. 사용자 정의 객체
    사용자 정의 객체는 표준 빌트인 객체와 호스트 객체처럼 기본 제공되는 객체가 아닌 사용자가 직접 정의한 객체를 말한다.

 *실행환경 : 브라우저 또는 Node.js 환경을 말한다.

**/

